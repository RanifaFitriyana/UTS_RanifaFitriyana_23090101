bil_pertama = int(input("Masukkan Bilangan Pertama : "))
bil_kedua = int(input("Masukkan Bilangan Kedua : "))

tambah = bil_pertama + bil_kedua
kurang = bil_pertama - bil_kedua
modulus = bil_pertama % bil_kedua

print(f'Hasil penjumlahan dari {bil_pertama} + {bil_kedua} = {tambah}')
print(f'Hasil pengurangan dari {bil_pertama} - {bil_kedua} = {kurang}')
print(f'Hasil modulus dari {bil_pertama} % {bil_kedua} = {modulus}')